/**
 * Tipos de dados
- Principais tipos de dados utilizados no JavaScript
- Number, String, Boolean, null e undefined, Object;
- Verificar o tipo de dado com o operador typeof;
 */
console.log("--------String-------");
let frase = "Sou Programador"
console.log(frase)
console.log(typeof frase)

console.log("--------Number-------");
let numero = 5;
console.log(numero);
console.log(typeof numero);


console.log("--------Float-------");
let float = 10.7;
console.log(float);
console.log(typeof float);


console.log("--------Boolean-------");
let feliz = true
console.log(typeof feliz);

let pobre = false
console.log(typeof pobre);


console.log("--------NaN - Infinity-------");
console.log(typeof NaN);
console.log(typeof +Infinity);
console.log(typeof -Infinity);
