
/**
 * Aula 20 - Operadores de atribuição
- Temos algumas maneiras de atribuir um valor a uma variavel
- As mais utilizadas são: +=,-=, *=, /=;
- Forma reduzida da operação x = x + 1;
- Em loops també é comum utilizar os operadores ++ ou --;
 */

console.log("\n----Operadores de atribuição são: +=,-=, *=, /=; ----\n");

let x = 1;
let y = 2;

// soma
console.log(x = x + y );
console.log(x += y );

// subtração
console.log(x -= y );

//multiplicação
console.log(x *= y );

//Divisão
console.log(x /= y );

// loops
console.log(x++);
console.log(x-- );


